package week03_24_09_2022;

public class IntroPostIncrementDecrement {

    public static void main(String[] args) {

/*
Task 1 : Create a class which is "IntroPostIncrementDecrement" define a variable,

          int a=5;
          print a;
          print the a++;
          print the a--;
 */

        int a=5;
        System.out.println(a); // 5
        System.out.println(a++); // 5 (it will go to 6 in the next line)
        System.out.println(a--); // 6 (it will go down to 5 in the next line)
        System.out.println("a = " + a); // 5





    }
}
